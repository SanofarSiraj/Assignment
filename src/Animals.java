public class Animals {
 // Name, age, size

    String Name;
    int Age;
    int size;


    public Animals() {
        this.Name=Name;
        this.Age=Age;
        this.size=size;

    }

    public void setName(String N){
        this.Name= N;

    }
    public void setAge(int A){
        this.Age= A;

    }

    public void setsize(int s){
        this.size= s;

    }
    public String getName()
    {
        return Name;
    }
    public int getAge()
    {
        return Age;
    }
    public int getsize()
    {
        return size;
    }
    }
